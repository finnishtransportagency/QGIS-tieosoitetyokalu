    def convert_coordinates_to_XY(self, linestring):
        xy_points = []
        if len(linestring) < 2:
            xy_points.append(QgsPointXY(linestring[0], linestring[1]))
        for coordinates in linestring:
            xy_points.append(QgsPointXY(coordinates[0], coordinates[1]))
        return xy_points

        
